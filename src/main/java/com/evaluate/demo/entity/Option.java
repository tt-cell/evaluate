package com.evaluate.demo.entity;

public class Option {
    private int options_id;
    private String options_name;
    private int options_score;


    public int getOptions_id() {
        return options_id;
    }

    public void setOptions_id(int options_id) {
        this.options_id = options_id;
    }

    public String getOptions_name() {
        return options_name;
    }

    public void setOptions_name(String options_name) {
        this.options_name = options_name;
    }

    public int getOptions_score() {
        return options_score;
    }

    public void setOptions_score(int options_score) {
        this.options_score = options_score;
    }



}

